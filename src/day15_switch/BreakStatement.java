package day15_switch;

public class BreakStatement {
    public static void main(String[] args) {
        int i = 3;

        switch (i){

            case 8:
                System.out.println("8");
                break;
            case 9:
                System.out.println("9");
            case 10:
                System.out.println("10");
        }
    }
}
