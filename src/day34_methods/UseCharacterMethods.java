package day34_methods;

public class UseCharacterMethods {
    public static void main(String[] args) {

        System.out.println("Letters from A to Z Uppercase:");
        GetCharacters.lettersFromAToZUpper();
        System.out.println("Letters from a to z Lowercase:");
        GetCharacters.lettersAToZLower();
        System.out.println("Letters from Z to A Uppercase:");
        GetCharacters.lettersZToAUpper();
        System.out.println("Letters from z to a Lowercase:");
        GetCharacters.lettersZtoALower();
        System.out.println("Numbers from 0 to 9:");
        GetCharacters.numbersFrom0To9();
    }
}
